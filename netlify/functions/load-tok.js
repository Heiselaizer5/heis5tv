    const resp = await fetch(SUPABASE_URL + '/rest/v1/azam_config?select=*&limit=1', {
      headers: { 'apikey': SUPABASE_ANON_KEY, 'Authorization': 'Bearer ' + SUPABASE_ANON_KEY, 'Accept': 'application/json' }
    });
